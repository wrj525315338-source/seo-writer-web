function extractArticleMarkdownForAudit(content: string): string {
  const fenced = content.match(/```(?:markdown|md)?\s*([\s\S]*?)```/i);
  if (fenced?.[1]) {
    return fenced[1].trim();
  }
  const trimmed = content.trim();
  const starts = [
    trimmed.search(/\*\*SEO Title\*\*:/i),
    trimmed.search(/^#\s+/m)
  ].filter((index) => index >= 0);
  if (starts.length > 0) {
    const start = Math.min(...starts);
    return trimmed.slice(start).trim();
  }
  return trimmed;
}

function stripSeoMetadataForWordCount(markdown: string): string {
  return markdown
    .split(/\r?\n/)
    .filter((line) => {
      const normalized = line.trim().replace(/\*\*/g, "").toLowerCase();
      return !/^(seo\s+title|title|description|meta\s+title|meta\s+description|url|url\s+slug|slug)\s*:/.test(normalized);
    })
    .join("\n");
}

function countVisibleArticleWords(markdown: string): number {
  const visibleText = stripSeoMetadataForWordCount(extractArticleMarkdownForAudit(markdown))
    .replace(/```[\s\S]*?```/g, " ")
    .replace(/!\[[^\]]*]\([^)]*\)/g, " ")
    .replace(/\[([^\]]+)]\([^)]+\)/g, "$1")
    .replace(/[`*_>#|~=[\]{}()]/g, " ")
    .replace(/^-{3,}$/gm, " ")
    .replace(/\s+/g, " ")
    .trim();
  const latinWords = visibleText.match(/[A-Za-z0-9]+(?:[’'-][A-Za-z0-9]+)*/g) || [];
  const cjkChars = visibleText.match(/[\u3400-\u9fff]/g) || [];
  if (latinWords.length === 0 && cjkChars.length > 0) {
    return cjkChars.length;
  }
  return latinWords.length + Math.round(cjkChars.length / 2);
}

