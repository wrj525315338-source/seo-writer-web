function walkFiles(root: string): string[] {
  if (!fs.existsSync(root)) {
    return [];
  }
  const files: string[] = [];
  for (const entry of fs.readdirSync(root, { withFileTypes: true })) {
    const fullPath = path.join(root, entry.name);
    if (entry.isDirectory()) {
      files.push(...walkFiles(fullPath));
    } else if (entry.isFile()) {
      files.push(fullPath);
    }
  }
  return files;
}

function readUInt24LE(buffer: Buffer, offset: number): number {
  return buffer[offset] + (buffer[offset + 1] << 8) + (buffer[offset + 2] << 16);
}

function imageDimensions(filePath: string): string {
  try {
    const buffer = fs.readFileSync(filePath);
    if (buffer.length >= 24 && buffer.subarray(0, 8).equals(Buffer.from([0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a]))) {
      return `${buffer.readUInt32BE(16)}x${buffer.readUInt32BE(20)}`;
    }
    if (buffer.length >= 12 && buffer.subarray(0, 4).toString("ascii") === "RIFF" && buffer.subarray(8, 12).toString("ascii") === "WEBP") {
      const chunk = buffer.subarray(12, 16).toString("ascii");
      if (chunk === "VP8X" && buffer.length >= 30) {
        return `${readUInt24LE(buffer, 24) + 1}x${readUInt24LE(buffer, 27) + 1}`;
      }
      if (chunk === "VP8L" && buffer.length >= 25) {
        const bits = buffer[21] | (buffer[22] << 8) | (buffer[23] << 16) | (buffer[24] << 24);
        return `${(bits & 0x3fff) + 1}x${((bits >> 14) & 0x3fff) + 1}`;
      }
      if (chunk === "VP8 ") {
        const startCode = Buffer.from([0x9d, 0x01, 0x2a]);
        const index = buffer.indexOf(startCode, 20);
        if (index >= 0 && buffer.length >= index + 7) {
          const width = buffer.readUInt16LE(index + 3) & 0x3fff;
          const height = buffer.readUInt16LE(index + 5) & 0x3fff;
          return `${width}x${height}`;
        }
      }
    }
    if (buffer.length > 4 && buffer[0] === 0xff && buffer[1] === 0xd8) {
      let offset = 2;
      while (offset < buffer.length) {
        if (buffer[offset] !== 0xff) {
          offset += 1;
          continue;
        }
        const marker = buffer[offset + 1];
        const length = buffer.readUInt16BE(offset + 2);
        if (marker >= 0xc0 && marker <= 0xc3 && buffer.length >= offset + 9) {
          return `${buffer.readUInt16BE(offset + 7)}x${buffer.readUInt16BE(offset + 5)}`;
        }
        offset += 2 + length;
      }
    }
  } catch {
    return "";
  }
  return "";
}

function tagsFromFilename(filePath: string): string {
  return path
    .basename(filePath, path.extname(filePath))
    .split(/[-_\s]+/)
    .map((item) => item.trim())
    .filter(Boolean)
    .join(", ");
}

function writeImageStyleReferenceExamples(project: Project): string {
  const examplesRoot = imageExamplesRoot();
  const output = outputPath(project.id, "image_style_reference_examples.md");
  const imageFiles = walkFiles(examplesRoot)
    .filter((filePath) => imageExampleExtensions.has(path.extname(filePath).toLowerCase()))
    .sort((a, b) => a.localeCompare(b, "en"))
    .slice(0, 50);

  const lines = [
    "# Image Style Reference Examples",
    "",
    "Use these local image examples as style and composition references during Phase 5.5 planning.",
    "Do not copy exact artwork, visible text, UI copy, or unsupported claims from these examples.",
    "Do not copy logos or wordmarks from reference examples.",
    "Translate the reusable visual traits into concrete prompt language: photorealistic generated learner, brand purple, soft bright background, phone mockup, purple chat bubbles, rounded cards, visual hierarchy, and article-specific composition.",
    "If an example conflicts with the active HelloTalk image policy, the policy wins.",
    "",
    `Examples root: ${examplesRoot}`,
    ""
  ];

  if (imageFiles.length === 0) {
    lines.push("No image examples were found.");
  } else {
    lines.push("| File | Relative path | Dimensions | Size | Filename tags |");
    lines.push("|---|---|---:|---:|---|");
    for (const filePath of imageFiles) {
      const stats = fs.statSync(filePath);
      const relativePath = path.relative(examplesRoot, filePath).replace(/\\/g, "/");
      const dimensions = imageDimensions(filePath) || "unknown";
      const sizeKb = `${Math.max(1, Math.round(stats.size / 1024))} KB`;
      lines.push(`| ${path.basename(filePath)} | ${relativePath} | ${dimensions} | ${sizeKb} | ${tagsFromFilename(filePath)} |`);
    }
  }

  fs.writeFileSync(output, lines.join("\n") + "\n", "utf-8");
  return output;
}

function resolveOptionalPath(raw: string, projectDir: string, outputsDir: string): string {
  const candidates = [
    raw,
    path.join(projectDir, raw),
    path.join(outputsDir, raw),
    path.resolve(process.cwd(), raw),
    path.resolve(process.cwd(), "..", raw)
  ].map((candidate) => path.resolve(candidate));
  const found = candidates.find((candidate) => fs.existsSync(candidate));
  if (!found) {
    throw new Error(`image_skill_path not found: ${raw}`);
  }
  return found;
}

