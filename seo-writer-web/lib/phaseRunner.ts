export async function runPhase4Chunked(projectId: string): Promise<void> {
  const project = requireProject(projectId);
  const state = readProjectState(projectId);
  const runId = createPhaseRun(projectId, "phase4", "running", "chunked audit");
  setPhaseStatus(projectId, "phase4", "running");
  updateProjectPhase(projectId, "phase4", "active");

  try {
    const writingChecklist = await ensureWritingChecklist(project);
    const full = normalizeFinalArticleForDelivery(project);
    const output = outputPath(project.id, phaseOutputs.phase4[0]);
    const articleWordCount = countVisibleArticleWords(fs.readFileSync(full, "utf-8"));
    const chunkPaths = preparePhase4Chunks(project.id, full);
    const reportDir = path.join(getOutputsDir(project.id), "phase4_audit_chunks");
    const chunkReportPaths: string[] = [];
    const sourceMtimeMs = Math.max(fs.statSync(full).mtimeMs, fs.statSync(writingChecklist).mtimeMs);

    for (const [index, chunkPath] of chunkPaths.entries()) {
      const chunkReport = path.join(reportDir, `chunk_${String(index + 1).padStart(2, "0")}_report.md`);
      const chunkSourceMtimeMs = Math.max(sourceMtimeMs, fs.statSync(chunkPath).mtimeMs);
      if (!canReusePhase4ChunkReport(chunkReport, chunkSourceMtimeMs)) {
        await runSkillPrompt(
          project,
          phase4PromptArgs("phase4_chunk_check.md", [writingChecklist, chunkPath], chunkReport),
          "auditor"
        );
      }
      ensureOutputExists(chunkReport);
      chunkReportPaths.push(chunkReport);
    }

    const wordRange = getWordCountRange(projectId);
    mergePhase4ChunkReports(chunkReportPaths, output, articleWordCount, full, writingChecklist, wordRange);
    ensureOutputExists(output);
    setPhaseStatus(projectId, "phase4", "waiting_review");
    updatePhaseRun(runId, "waiting_review", path.relative(getProjectDir(projectId), output).replace(/\\/g, "/"));
    updateProjectPhase(projectId, "phase4", "active");
  } catch (error) {
    const message = sanitizeError(error);
    setPhaseStatus(projectId, "phase4", "failed", message);
    updatePhaseRun(runId, "failed", "", message);
    updateProjectPhase(projectId, "phase4", "failed");
    throw new Error(message);
  }
}

async function runPromptPhase(project: Project, phase: PhaseId): Promise<string> {
  const outputs = getOutputsDir(project.id);
  const output = path.join(outputs, phaseOutputs[phase][0]);
  const summary = outputPath(project.id, phaseOutputs.phase0[0]);
  const writingChecklist = await ensureWritingChecklist(project);
  const outline = outputPath(project.id, phaseOutputs.phase1[0]);
  const firstTwo = outputPath(project.id, phaseOutputs.phase2[0]);
  const full = outputPath(project.id, phaseOutputs.phase3[0]);
  const checklist = outputPath(project.id, phaseOutputs.phase4[0]);
  const brief = path.join(getInputsDir(project.id), "article_brief.md");
  const extracted = outputPath(project.id, "extracted_materials.md");
  const outlineTemplate = skillPath("templates", "outline_template.md");

  const inputMap: Record<PhaseId, string[]> = {
    phase0: [],
    phase1: [summary, brief, extracted, outlineTemplate],
    phase2: [summary, outline, writingChecklist, languageContentRequirementsPath()],
    phase3: [summary, outline, firstTwo, brief, languageContentRequirementsPath()],
    phase4: [writingChecklist, full],
    phase5: [summary, full, checklist]
  };

  if (phase === "phase2") {
    const draft = outputPath(project.id, "02_first_two_sections_draft.md");
    await runSkillPrompt(project, commonPromptArgs(project.id, phase, inputMap[phase], draft), "writing");
    ensureOutputExists(draft);
    await runSkillPrompt(
      project,
      phase2AuditorPolishPromptArgs([writingChecklist, summary, outline, draft], output),
      "auditor"
    );
    return output;
  }

  if (phase === "phase4") {
    return runPhase4(project);
  }

  const args = commonPromptArgs(project.id, phase, inputMap[phase], output);
  await runSkillPrompt(project, args, "writing");

  // Phase 3: validate word count after generation
  if (phase === "phase3") {
    const articleContent = fs.readFileSync(output, "utf-8");
    const wordCount = countVisibleArticleWords(articleContent);
    const wordRange = getWordCountRange(project.id);
    if (wordCount < wordRange.min) {
      // Attempt word count repair (up to 2 retries)
      for (let attempt = 0; attempt < 2; attempt++) {
        const repairInput = outputPath(project.id, `phase3_repair_input_${attempt}.md`);
        fs.writeFileSync(repairInput, [
          "# Word Count Repair",
          "",
          `Current word count: ${wordCount} words`,
          `Target range: ${wordRange.min}-${wordRange.max} words`,
          `Deficit: ${wordRange.min - wordCount} words below minimum`,
          "",
          "## Current Article",
          "",
          articleContent,
        ].join("\n"), "utf-8");
        try {
          await runSkillPrompt(project, [
            "scripts/run_prompt.py",
            "--prompt", "prompts/phase3_length_repair.md",
            "--input", repairInput,
            "--output", output,
          ], "writing");
          const repaired = fs.readFileSync(output, "utf-8");
          const repairedCount = countVisibleArticleWords(repaired);
          if (repairedCount >= wordRange.min) break;
        } catch {
          // repair failed, keep original
        }
      }
    }
  }

  return output;
}

export async function runPhase(projectId: string, phase: PhaseId): Promise<void> {
  const project = requireProject(projectId);
  const state = readProjectState(projectId);
  assertPhaseCanRun(state, phase);
  const phaseState = state.phases[phase];
  if (phaseState.status === "running" || phaseState.status === "waiting_review" || phaseState.status === "approved") {
    return;
  }
  const invocation = buildInvocationPrompt(projectId, phase);
  const runId = createPhaseRun(projectId, phase, "running", invocation);
  setPhaseStatus(projectId, phase, "running");
  updateProjectPhase(projectId, phase, "active");

  try {
    const output = phase === "phase0" ? await runPhase0(project) : phase === "phase5" ? await runPhase5(project) : await runPromptPhase(project, phase);
    ensureOutputExists(output);
    if (requiresManualReview(phase)) {
      setPhaseStatus(projectId, phase, "waiting_review");
      updatePhaseRun(runId, "waiting_review", path.relative(getProjectDir(projectId), output).replace(/\\/g, "/"));
      updateProjectPhase(projectId, phase, "active");
    } else {
      const nextState = approvePhaseInState(projectId, phase);
      updatePhaseRun(runId, "approved");
      updateProjectPhase(projectId, nextState.currentPhase, "active");
    }
  } catch (error) {
    const message = sanitizeError(error);
    setPhaseStatus(projectId, phase, "failed", message);
    updatePhaseRun(runId, "failed", "", message);
    updateProjectPhase(projectId, phase, "failed");
    throw new Error(message);
  }
}

export async function revisePhase(projectId: string, phase: PhaseId, userComment: string): Promise<void> {
  if (!requiresManualReview(phase)) {
    throw new Error("该阶段不需要人工审查，不能提交修改意见。");
  }
  const project = requireProject(projectId);
  if (phase === "phase5") {
    await revisePhase5(project, userComment);
    return;
  }
  const currentOutput = readOutputForPhase(projectId, phase);
  if (!currentOutput) {
    throw new Error("当前阶段还没有可修订的输出。");
  }
  const output = outputPath(projectId, phaseOutputs[phase].find((file) => file.endsWith(".md")) || phaseOutputs[phase][0]);
  const revisionPrompt = outputPath(projectId, `revise_${phase}_prompt.md`);
  fs.writeFileSync(
    revisionPrompt,
    [
      `# Revise ${phase}`,
      "",
      "请使用 SEO Article Writer Skill 根据用户修改意见修订当前阶段输出。",
      "写作规范仍然是最高优先级。如果用户意见和规范冲突，请使用 Skill 中的冲突处理格式，不要绕过规范。",
      "",
      "## 用户修改意见",
      "",
      userComment,
      "",
      "## 输出要求",
      "",
      `请更新 ${path.basename(output)}。完成后停止，不要进入下一阶段。`
    ].join("\n"),
    "utf-8"
  );

  const runId = createPhaseRun(projectId, phase, "running", userComment);
  setPhaseStatus(projectId, phase, "running");
  try {
    const summary = outputPath(projectId, "00_material_reading_summary.md");
    await runSkillPrompt(
      project,
      commonPromptArgs(projectId, phase, [revisionPrompt, summary, output], output)
    );
    ensureOutputExists(output);
    setPhaseStatus(projectId, phase, "waiting_review");
    createReviewComment(projectId, phase, userComment, path.relative(getProjectDir(projectId), output).replace(/\\/g, "/"));
    updatePhaseRun(runId, "waiting_review", path.relative(getProjectDir(projectId), output).replace(/\\/g, "/"));
    updateProjectPhase(projectId, phase, "active");
  } catch (error) {
    const message = sanitizeError(error);
    setPhaseStatus(projectId, phase, "failed", message);
    updatePhaseRun(runId, "failed", "", message);
    updateProjectPhase(projectId, phase, "failed");
    throw new Error(message);
  }
}

