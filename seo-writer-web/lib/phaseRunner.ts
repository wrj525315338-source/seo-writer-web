async function runPhase6ImageOutput(
  project: Project,
  options: {
    mode?: "all" | "generate" | "insert";
    onlyImageId?: string;
    reviewComment?: string;
  } = {}
): Promise<void> {
  const outputsDir = getOutputsDir(project.id);
  const finalDocx = outputPath(project.id, "final_article_for_google_docs.docx");
  const configPath = writeImageGenerationConfig(project);
  const scriptPath = path.join(process.cwd(), "scripts", "phase6_image_generation.py");
  const logPath = outputPath(project.id, "phase6.log");
  const mode = options.mode || "all";
  const args = [
    scriptPath,
    "--config",
    configPath,
    "--project-dir",
    getProjectDir(project.id),
    "--final-docx",
    finalDocx,
    "--article-md",
    outputPath(project.id, "final_article_with_image_slots.md"),
    "--image-plan",
    outputPath(project.id, "image_plan.json"),
    "--output-dir",
    outputsDir,
    "--mode",
    mode
  ];
  if (options.onlyImageId) {
    args.push("--only-image-id", options.onlyImageId);
  }
  if (options.reviewComment) {
    args.push("--review-comment", options.reviewComment);
  }

  try {
    await runPythonScript(
      args,
      { cwd: process.cwd(), env: buildImageEnv(project), timeoutMs: 900000 }
    );
  } catch (error) {
    const message = sanitizeError(error);
    fs.appendFileSync(
      logPath,
      [
        `[${new Date().toISOString()}] Phase 6 runner failed before graceful completion.`,
        message,
        ""
      ].join("\n"),
      "utf-8"
    );
    const fallbackDocx = outputPath(project.id, "final_article_with_images.docx");
    if (mode === "all" && fs.existsSync(finalDocx) && !fs.existsSync(fallbackDocx)) {
      fs.copyFileSync(finalDocx, fallbackDocx);
    }
  }
}

